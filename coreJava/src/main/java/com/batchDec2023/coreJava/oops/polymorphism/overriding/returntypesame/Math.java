package com.batchDec2023.coreJava.oops.polymorphism.overriding.returntypesame;

/*

Overloading : we can change the return type [cause we change input paramters]
 */
public class Math {


    public int add(int a, int b){
        return a+b;
    }

    public double add(double a, double b){
        return a+b;
    }
}
